			<ol style="margin:5px; padding-left:20px;">
				<li style="color: red;"><b>NB!</b> If You already have ARX-Raamat installed <b>make backup of Your database file</b></li>
				<li>Download <a href="/v7/setup/ARX-Raamat7_Setup.exe">ARX-Raamat installer</a> and run it</li>
				<li>After opening ARX-Raamat fill registration form and send it</li>
				<li>Download <a href="/v7/setup/ARX-Raamat7_Voti.exe">Keyfile generator</a> and generate key file</li>
				<li>Save key file 'Raamat.dll' to program folder (usualy 'C:\Program Files\ARX-Raamat')</li>
				<li>Restart ARX-Raamat (default username is 'arx' and password is 'q')</li>
				<li>Add new user and delete user 'arx'</li>
