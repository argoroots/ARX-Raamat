			<h1>ARX-Raamat 7</h1>
	
			<h2>Overview</h2>
			<p style="margin:5px;">ARX-Raamat 7 is a program for managing Your library - Your books, schoolbooks, newspapers/magazines, audio-video items and work books. There is also place for authors, publishers, languages, media types etc. Its possible (and necessary for lending) to enter information about people (lenders) and their groups/classes. Of course its possible to set sort orders, filters and search data. You can lend items to people. You can monitor debtors and see lending and catalogue statistics. ARX-Raamat is designed specially for school libraries, but of course You can use it in any library if You like.</p>

