			<h1>ARX-Raamat 7</h1>
			<p style="margin:5px;">ARX-Raamat 7 on Microsoft Windows keskkonnas töötav raamatukogu infosüsteem, millega on võimalik teostada kõiki raamatukogus vajalikke operatsioone (andmete haldamine, laenutus, mahakandmine, statistika jm). Kuigi ARX-Raamat sisaldab spetsiaalselt kooliraamatukogu tööks vajalike osi (nt õpikute ja töövihikute haldamine), on seda võimalik edukalt kasutada ka muudes raamatukogudes.</p>
	
			<h2>Ülevaade</h2>
			<p style="margin:5px;"><b>Andmed:</b><br>ARX-Raamatu andmebaasi fond jaguneb raamatute, õpikute, perioodika, audio-video, töövihikute ja metoodilise kirjanduse vahel. Lisaks on andmed autorite, kirjastuste, liikide jm kohta. Võimalik (ja laenutamise jaoks vajalik) on sisestada andmed ka lugejate kohta. Lugejad saab jagada klassidesse. Andmeid on võimalik lisada, muuta, kustutada ja dubleerida. Samuti kõikide väljade järgi sorteerida, otsida ja filtreerida.<br><br><b>Toimingud:</b><br>Programm võimaldab lugejatele välja laenutada (ja jälle raamatukokku tagastada) kõiki fondis olevaid teavikuid. Saab vaadata ja välja trükkida võlglaste nimekirja või vaadata kelle käes on mingi raamat, õpik vm. teavik. Saab vaadata igapäevast laenutusstatistikat nii klasside kui ka meedia liikide kaupa ning vaadata muid statistilisi andmeid. Lisaks on võimalik teostada mahakandmist.</p>
		
